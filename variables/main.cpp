#include <iostream>
using namespace std;
int main(){
    //math operator
    cout<<3+4<<endl; //addition
    cout<<5-2<<endl; //subtraction
    cout<<9*2<<endl; //multiplication
    //be careful of the following
    
    cout<<-11.0/2<<endl; //division
    //be careful of integer division
    //integer division-->if both numbers are integer (no decimal)
    //answer from division will be without decimal
    //to counter that, you need to put a decimal back to 
    //one of the number
    
    cout<<23%4<<endl; //remainder of 5/4
    return 0;
}
